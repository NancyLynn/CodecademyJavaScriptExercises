const myAge=38;
//Sets the constant variable to my current age.

let earlyYears=2;
//The first two human years of a dog's life count as 10.5 dog years each. This variable will change.

earlyYears *=10.5;
let laterYears=(myAge-2);
//We accounted for the ealy years, now subtract them from the total.

laterYears *=4;
//Each human year after the first two counts as 4 dog years.

let myAgeInDogYears=(laterYears + earlyYears);
//Adding both parts together

const myName = "Laina Gray";
myName.toLowerCase;
//Change the contents of the myName variable to all lower case.

console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`);
